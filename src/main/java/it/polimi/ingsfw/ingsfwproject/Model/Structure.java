package it.polimi.ingsfw.ingsfwproject.Model;

/**
 * Enum Structure
 *
 * Description: This enum represents different possible structures required by some objective cards, the structured ones.
 */
public enum Structure {
    RIGHT_DIAGONAL,      // Represents a right diagonal structure
    DOUBLE_UP_LEFT,      // Represents a double up left structure
    LEFT_DIAGONAL,       // Represents a left diagonal structure
    DOUBLE_DOWN_LEFT,    // Represents a double down left structure
    DOUBLE_DOWN_RIGHT,   // Represents a double down right structure
    DOUBLE_UP_RIGHT      // Represents a double up right structure
}
