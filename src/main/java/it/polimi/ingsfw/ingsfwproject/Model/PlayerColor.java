package it.polimi.ingsfw.ingsfwproject.Model;

import java.io.Serializable;

/**
 * The {@code PlayerColor} enum represents the different colors that a player can choose in the game.
 * This enum implements {@code Serializable} to allow player colors to be serialized.
 */
public enum PlayerColor implements Serializable {
    YELLOW,
    GREEN,
    RED,
    BLUE,
}
